// This function to show hidden elements.
function showElement() {
    var showElement = document.getElementById("hiddenElement");
    showElement.style.display = "block";
    showElement.style.fontFamily = "'Segoe UI', Tahoma, Geneva, Verdana, sans-serif";
}

// This function to hidden show elements.
function hiddenElement() {
    var hiddenElement = document.getElementById("hiddenElement");
    hiddenElement.style.display = "none";
}

